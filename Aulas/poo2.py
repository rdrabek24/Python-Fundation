
class Servidor():
    

    def __init__(self):
        self.cpu = 2
        self.memoria = 2048
        self.disco = 500

def inserirMemoria(self, mem):
    self.memoria += mem

def inserirCPU(self, cpu):
    self.cpu += cpu

def inserirDisco(self, disco):
    self.disco += disco

def removeMemoria(self, mem):
    self.memoria -= mem

def removeDisco(self, disco):
    self.disco -= disco

def removeCPU(self, cpu):
    self.cpu -= cpu 



